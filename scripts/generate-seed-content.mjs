#!/usr/bin/env node
// One-shot authoring tool for the demo seed.
//
// Generates 40 (prompt, content) tuples by calling the live prompt-generation
// template (lib/prompts/journal-prompt-generation.md) for each prompt and a
// matching content-generation template (scripts/seed-content-generation.md)
// for each entry, in chronological order so each call sees the growing
// history. Writes the result to prisma/seed-entries.ts as a TypeScript const
// array imported by prisma/seed.ts.
//
// Run from the repo root with ANTHROPIC_API_KEY set (in .env.local or env):
//   node scripts/generate-seed-content.mjs
// Takes ~15-20 minutes and spends real Anthropic tokens (~$1-3 of Sonnet 4.6).
// Re-run only when the persona, prompt-gen template, or content-gen template
// changes meaningfully — day-to-day, npm run db:seed reads the baked output.

import { spawnSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import process from 'node:process'
import Anthropic from '@anthropic-ai/sdk'
import { config } from 'dotenv'

config({ path: '.env.local' })

if (!process.env.ANTHROPIC_API_KEY) {
  console.error('ANTHROPIC_API_KEY is required (set in .env.local or environment).')
  process.exit(1)
}

// Matches lib/anthropic.ts CONTENT_MODEL — Sonnet 4.6 is fast and cheap and
// produces journal entries indistinguishable from Opus for this task.
const MODEL = 'claude-sonnet-4-6'

const PROMPT_TEMPLATE = readFileSync('lib/prompts/journal-prompt-generation.md', 'utf-8')
const CONTENT_TEMPLATE = readFileSync('scripts/seed-content-generation.md', 'utf-8')

// 40 entries spread across 60 days, oldest first. Cadence is uneven on
// purpose — clusters of journaling followed by quieter stretches mirror how
// people actually keep a practice. Most recent entries cluster (0,1,2,3) so
// the demo feels active when someone signs in today.
const DAYS_AGO_SCHEDULE = [
  60, 58, 57, 55, 53, 52, 50, 48, 47, 46, 44, 42, 41, 39, 37, 36, 35, 33, 31, 30, 28, 27, 25, 23,
  22, 20, 19, 17, 15, 14, 12, 10, 9, 8, 6, 5, 3, 2, 1, 0,
]

const OUTPUT_PATH = 'prisma/seed-entries.ts'

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })

function renderHistory(history) {
  if (history.length === 0) {
    return '(no prior entries — this is the first entry the user has written.)'
  }
  return history
    .map(
      (e, i) =>
        `### Entry ${i + 1} (${e.daysAgo} days ago)\n\nPrompt: ${e.prompt}\n\n${e.content}\n`
    )
    .join('\n---\n\n')
}

function renderTemplate(template, vars) {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => vars[key] ?? '')
}

async function generatePrompt(history) {
  const systemPrompt = renderTemplate(PROMPT_TEMPLATE, {
    RECENT_ENTRIES: renderHistory(history),
  })
  const response = await client.messages.create({
    model: MODEL,
    max_tokens: 300,
    system: systemPrompt,
    messages: [{ role: 'user', content: 'Generate the next prompt.' }],
  })
  const text = response.content
    .filter((b) => b.type === 'text')
    .map((b) => b.text)
    .join('')
    .trim()
  // The template says "Return ONLY the prompt text — no preamble, no quotes
  // around it" but models sometimes wrap anyway. Strip a single set of outer
  // quotes if present.
  return text.replace(/^["“'](.+?)["”']$/s, '$1').trim()
}

async function generateContent(prompt, history) {
  const systemPrompt = renderTemplate(CONTENT_TEMPLATE, {
    HISTORY: renderHistory(history),
    PROMPT: prompt,
  })
  const response = await client.messages.create({
    model: MODEL,
    max_tokens: 4096,
    system: systemPrompt,
    messages: [{ role: 'user', content: 'Write the entry.' }],
  })
  return response.content
    .filter((b) => b.type === 'text')
    .map((b) => b.text)
    .join('')
    .trim()
}

function emit(history) {
  const generatedAt = new Date().toISOString()
  const body = `// AUTO-GENERATED by scripts/generate-seed-content.mjs at ${generatedAt}.
// Do not hand-edit. Re-run that script to regenerate from the live prompt-gen
// template (lib/prompts/journal-prompt-generation.md) plus the persona in
// scripts/seed-content-generation.md.

export type SeedEntry = {
  prompt: string
  content: string
  daysAgo: number
}

export const SEED_ENTRIES: readonly SeedEntry[] = ${JSON.stringify(history, null, 2)} as const
`
  writeFileSync(OUTPUT_PATH, body)
}

async function main() {
  const history = []
  const total = DAYS_AGO_SCHEDULE.length
  for (let i = 0; i < total; i++) {
    const daysAgo = DAYS_AGO_SCHEDULE[i]
    process.stderr.write(
      `[${String(i + 1).padStart(2, ' ')}/${total}] daysAgo=${String(daysAgo).padStart(2, ' ')}  `
    )
    const prompt = await generatePrompt(history)
    process.stderr.write(`prompt(${prompt.length}c)  `)
    const content = await generateContent(prompt, history)
    const wordCount = content.trim().split(/\s+/).length
    process.stderr.write(`content(${content.length}c, ${wordCount}w)\n`)
    history.push({ prompt, content, daysAgo })
    // Save after every entry so a crash mid-run leaves usable partial output.
    emit(history)
  }
  process.stderr.write(`\nWrote ${history.length} entries to ${OUTPUT_PATH}\n`)

  // Run biome over the generated file so it lands formatted.
  const fmt = spawnSync('npx', ['biome', 'format', '--write', OUTPUT_PATH], { stdio: 'inherit' })
  if (fmt.status !== 0) {
    process.stderr.write(`(biome format exited ${fmt.status}; file is still valid TypeScript)\n`)
  }
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
